#include "corewar.h"

int32_t	big_endian(int32_t little)
{
	int32_t	big;
	int32_t	x;

	x = little;
	big = ((x >> 24) & 0xff)
		| ((x << 8) & 0xff0000)
		| ((x >> 8) & 0xff00)
		| ((x << 24) & 0xff000000);
	return (big);
}
