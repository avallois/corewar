#include "asm.h"

int32_t		little_endian(int32_t big)
{
	int32_t		little;
	int32_t		num;

	num = big;
	little = ((num >> 24) & 0xff)
		| ((num << 8) & 0xff0000)
		| ((num >> 8) & 0xff00)
		| ((num << 24) & 0xff000000);
	return (little);
}
